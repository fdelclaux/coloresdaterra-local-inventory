    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
    String amount1 = AmountBought.getText();
    String cost1 = Cost.getText();
    String description = Description.getText();
    String location = Location.getText();
    if(!(image == null)){
        Path relPath = Paths.get("resources/Images/"+image.getName()); 
        try {
            Files.copy(image.toPath(), relPath); 
        } 
        catch (IOException ex) {
            Logger.getLogger(NewItem.class.getName()).log(Level.SEVERE, null, ex); 
        }
    } 
    if(description.equals("")||amount1.equals("")||cost1.equals("")||location.equals("")){
        JOptionPane.showMessageDialog(this,
            "Please fill Description, Amount, Cost, and Location",
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    } 
    else{
        boolean success = false;
        boolean fail = false;
        try{
            int amount = Integer.parseInt(amount1); 
            double cost = Double.parseDouble(cost1); 
            if(Integer.parseInt(amount1)<1){
                JOptionPane.showMessageDialog(this,
                "Please enter an integer amount greater or equal to one",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            }
            success = Database.Inventory.firstTimeAdd(description, amount, cost, location, image); 
        }
        catch(Exception e){
            System.out.println(e);
            fail = true; 
            JOptionPane.showMessageDialog(this,
                "Please fill in the amount and cost fields appropriately", "Error",
                JOptionPane.ERROR_MESSAGE);
        }
        if(success){
            JOptionPane.showMessageDialog(this, 
                "Your item has been succesfully added to your inventory");
        }
        else if(!fail) {
            JOptionPane.showMessageDialog(this, 
                "There was an error adding your item, make sure all the fields are filled in correctly",
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }

        // TODO add your handling code here:
    }//GEN-LAST:event_AddActionPerformed

